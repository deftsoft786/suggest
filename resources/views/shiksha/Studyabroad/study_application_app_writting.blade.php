		<h3 itemprop="headline name" class="sop-title" style="color:#004080; text-align:center; font-size:30px;margin-top:40px">Statement of Purpose (SOP):  All you need to know!</h3>
		<div class="dyanamic-content" itemprop="articleBody">
	<p style="font-size:18px;padding-left:15px; margin-top:15px; color:#004080;">Statement of Purpose is a long essay that is often asked by universities abroad. The essay, usually about 1000 words, seeks to understand the candidate’s life, the motivations for the chosen career path and his/her goals.</p>
         <center><img src="{{url('shiksha/assets/images/home_rotate_01.jpg')}}"></center>

<p>&nbsp;</p>
<p style="font-size:16px; padding-left:15px;color:#004080;">There are many elements to a Statement of Purpose or a SOP as it is famously referred to. The Universities could ask question-based essays or simply to present your statement of purpose. Until and unless categorically asked, a SOP must include your goals, the career path you have taken up so far as well as your academic progress. Other elements that are further important to the SOP are also the personal motivations that&nbsp;lead you to choose the university/course you have applied to as well as how you intend to use that experience to achieve that goal.</p>

<p style="font-size:16px; padding-left:15px;color:#004080;">As is often pointed out, a well-written essay/ statement of purpose is extremely critical to your admission. Needless to say, there are many aspects of a candidate’s application that are inspected before finalizing a decision. While the academic record and other exam score cards are essentially objective in nature, am SOP is the only truly subjective aspect of your application. It is, simply put, the only document in your application that gives you the opportunity to prove that you have something unique that makes you stand out from the crowd. As such, it is the document of your application docket that can hugely determine your admission.</p>	<br>

