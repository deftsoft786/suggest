<h3 dir="ltr" style="color:#004080; font-size:18px; margin-left:20px;">TOEFL Result</h3>
<p dir="ltr">You will receive an email when your scores are available, which would be 10 days after your test date. You can log in to your TOEFL iBT account on the TOEFL Registration website to see your scores. Printed score reports will be mailed approximately 13 days after your test date.</p>
<p dir="ltr">Your scores are valid for two years and can be sent to other higher education institutions during that time.</p>
<h2><a href="https://studyabroad.shiksha.com/exams/toefl/important-dates" target="_blank">TOEFL Dates</a></h2>
<p dir="ltr">Your target date for taking the TOEFL exam should be at least a month before your first application deadline. This will give you plenty of time to work on your applications, and in case you need to retake the exam you can do so after 12 days of the first attempt.</p>
<p dir="ltr">So if the deadline for your application is November, you should take the exam by October. If you feel the need to retake TOEFL, you can take it in the latter half of October again. You can retake the TOEFL after 12 days post your first attempt.&nbsp;<a href="https://studyabroad.shiksha.com/exams/toefl/important-dates" target="_blank">See city wise TOEFL important dates here</a>.</p>
